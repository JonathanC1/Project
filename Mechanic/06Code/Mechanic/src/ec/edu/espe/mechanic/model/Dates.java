package ec.edu.espe.mechanic.model;

import java.util.Date;

/**
 *
 * @author ELEN
 */
public class Dates {
    private Date entry;
    private Date exit;

    /**
     * @return the entry
     */
    public Date getEntry() {
        return entry;
    }

    /**
     * @param entry the entry to set
     */
    public void setEntry(Date entry) {
        this.entry = entry;
    }

    /**
     * @return the exit
     */
    public Date getExit() {
        return exit;
    }

    /**
     * @param exit the exit to set
     */
    public void setExit(Date exit) {
        this.exit = exit;
    }
    
    
}
